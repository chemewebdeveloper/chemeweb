<h2>
	A Welcoming Environment</h2>
<p>
	The chemical engineering graduate program averages around 45 students, or approximately 3 students per faculty member.&nbsp; Students will get to know nearly all the faculty, staff, and their fellow students while attending socials and scientific seminars and in day-to-day interactions in hallways, classrooms, and laboratories. In recent years, around one-fifth of our students are women, and one-third are international students.&nbsp; In 2009, the program included international students from China, India, Singapore, Iran, and Canada.</p>
<p>
	BYU is a very family-friendly environment. &nbsp;Around two-thirds of our graduate students are married and many also have children.</p>
<p>
	BYU as a whole is well known for its international reach, with students (graduate and undergraduate) from all 50 states, and 115 countries. &nbsp;More than 75% of BYU students speak a second language. &nbsp;Additional information is at the BYU <a href="http://yfacts.byu.edu/viewarticle.aspx?id=135" target="_blank">demographics webpage</a>.</p>
<h2>
	Honor Code</h2>
<p>
	BYU is sponsored by the Church of Jesus Christ of Latter-Day Saints.&nbsp; Many students choose to attend BYU over academically comparable programs because of the BYU <a href="http://saas.byu.edu/catalog/2009-2010ucat/GeneralInfo/HonorCode.php" target="_blank">Honor Code</a> and the wholesome social atmosphere.&nbsp; Under the Honor Code, all students who attend Brigham Young University agree to observe high moral standards that include chaste living, modesty in dress, and refraining from using tobacco, alcohol, coffee, and illegal or harmful drugs.&nbsp; About 20% of the department&#39;s graduate students are not members of the Church.</p>
