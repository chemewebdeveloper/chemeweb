			<h2>
	High Academic Quality</h2>
<p>
	The chemical engineering department has offered graduate degrees since 1962 and is home to world-class research facilities and researchers.&nbsp; We have strong research programs in sustainable energy, combustion, catalysis, <span data-scayt_word="thermophysical" data-scaytid="1">thermophysical</span> properties, biochemical and biomedical engineering, and electrochemical engineering.&nbsp; All of these areas include experimental investigations and computer modeling of complex molecular and physical systems.&nbsp;&nbsp; With multiple faculty members in each area, graduate students have many options when choosing faculty advisors and research topics.</p>
<p>
	BYU is home to <span data-scayt_word="ACERC" data-scaytid="3">ACERC</span>, an internationally recognized center for combustion research, and <span data-scayt_word="DIPPR" data-scaytid="4">DIPPR</span>, a premier <span data-scayt_word="thermophysical" data-scaytid="2">thermophysical</span> properties database managed for the American Institute of Chemical Engineers.&nbsp; Students benefit from being at a large university (30,000 students) with strong departments in a range of scientific and engineering disciplines.&nbsp; The University library is ranked 3rd best college library in the US, after Harvard and Princeton.&nbsp; The Fulton supercomputer facility is ranked among the top five university <span data-scayt_word="supercomputers" data-scaytid="6">supercomputers</span> in the nation.</p>
<p>
	All 15 full-time faculty members are active in research and receive on average $250,000 per faculty member per year to support student research.&nbsp; The median time to graduation for MS students is 2 years and for PhD students is 4.5 years.&nbsp; For the PhD this is considerably less than graduation times at many other programs in the US.&nbsp; Faculty employ significant numbers of undergraduate students to assist graduate students in their work, which provides graduate students with experience in mentoring and leading others as part of a research team.</p>
<p>
	Our graduates receive great opportunities for employment.&nbsp; All of the MS and PhD graduates from our program are able to secure rewarding jobs that use their unique skills, such as at industrial research centers, national laboratories, and universities.</p>
