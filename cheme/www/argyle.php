			<p>Argyle left industry in 1998 to attend graduate school at the University of California at Berkeley, where 
			he was advised by Professors Alexis T. Bell and Enrique Iglesia and received his Ph.D. in Chemical Engineering 
			in 2003. His dissertation research involved synthesis, characterization, and kinetic/mechanistic evaluation of 
			metal oxide heterogeneous catalysts for the oxidative dehydrogenation of light alkanes to form alkenes.</p>
			<p>In 2003, Argyle became an Assistant Professor in the Department of Chemical and Petroleum Engineering at the
			University of Wyoming, where he was promoted to Associate Professor and served as Department Head in 2008-2009. He 
			has taught classes on material and energy balances, process simulation and engineering economics, portions of senior 
			design, kinetics and chemical reaction engineering, fluid dynamics, and an introduction to engineering computing.</p>
			<p>Argyle's research interests lie in determining the structure/function relationships of heterogeneous catalysts using 
			spectroscopic techniques, plasma reaction engineering, coal gasification, and new methods for carbon dioxide capture and storage.</p>
