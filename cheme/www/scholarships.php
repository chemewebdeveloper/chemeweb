		<h2>Returned-Missionary Awards</h2>
			<p>
			A $100 award is available for newly-returned missionaries who enroll as chemical
			engineering majors their first semester after their mission. Applications are due
			April 1st and are available in the department office (350 CB).  This scholarship
			will be awarded in the first or second Fall semester following return to BYU
			(depending on application by the prior April 1 deadline - see the link Planning
			a Mission? or Returning Missionaries). The application form can be obtained from
			the Chemical Engineering Department office or can be downloaded from the link at
			the bottom of this page.
			</p>
		<h2>Dr. L. Douglas and Marian Bird Smoot Scholarship</h2>
			<p>
			This scholarship is given to a limited number of students who have returned from
			a mission and who commit to an engineering or technology major (preference given
			to Chemical Engineering majors). The award is $1000 or 1/3 LDS tuition for one
			semester, whichever is greater, and is awarded based on a combination of grade
			point average (minimum 3.0 required) and financial need. The scholarship is
			awarded in the first or second year back at BYU after returning from the mission
			(depending on application by the prior April 1 deadline - see the link Planning
			a Mission? or Returning Missionaries).  The application form can be obtained from
			the Chemical Engineering Department office or can be downloaded from the link at
			the bottom of this page.
			</p>
		<h2>Undergraduate Scholarship for Continuing Students</h2>
			<p>
			Several awards for junior and senior chemical engineering
			majors have been provided by outside industry and individual
			donations. These awards, based on GPA, need, and extracurricular
			activities, are administered by the department on a semester-by-semester basis.
			Award amounts vary, but are generally one-quarter tuition. Applications are
			available below or from the department office (350 CB) and should be turned
			in by April 1 for the following Fall and Winter semesters. The application
			form can be obtained from the Chemical Engineering Department office or can
			be downloaded from the link at the bottom of this page.
			</p>
		<h2>Research Work</h2>
			<p>
			While not a scholarship or grant, a research project can be a stimulating and
			educational way to finance schooling. Many of the chemical engineering faculty
			have research funding available for interested students. Application is made by
			personal discussion with appropriate faculty. Pay scales vary depending upon
			class and experience.
			</p>
