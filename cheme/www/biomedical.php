			 and can break off into the bloodstream and block downstream blood capillaries.
			    Dr. Solen’s group experimentally studies the effects of hemodynamics and 
			    drugs on these phenomena using miniature flow cells monitored with 
			    videomicroscopy and laser light-scattering. They also use computer models
			     to predict blood-biomaterial interactions in medical devices, which may
			      help to refine device designs.</p>
			
			
