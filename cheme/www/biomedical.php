<h3>Biomedical</h3><br />
	<h3>Finding The Solutions</h3>
	<p colspan="2">Biomedical engineering is a prominent specialty of several engineering
	 fields, including chemical engineering. Specifically, biomedical engineering 
	 integrates physical, chemical, biological, mathematical, and computational 
	 principles to address medical problems. It creates knowledge from the molecular
	  to the organ system levels and develops innovative biologics, materials, processes,
	   implants, devices, and informatics for the prevention, diagnosis, and treatment 
	   of disease, for patient rehabilitation, and for improving health.</p>
	
