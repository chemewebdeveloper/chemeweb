		<p>Dr. Brad Bundy is an Assistant Professor of Chemical Engineering at Brigham Young University. 
		His research focuses on protein production and protein engineering. Dr. Bundy completed his Ph.D. and M.S.
		degrees at Stanford University under the direction of James R. Swartz. He is an alumni of the BYU Chemical 
		Engineering Department with a B.S. completed in 2004. Dr. Bundy received the 2010 and 2012 Outstanding Faculty 
		Teaching Award as determined by the chemical engineering student body. He is the recipient of the Research Awards 
		from the National Science Foundation and NASA and was awarded the Stanford Graduate Fellowship, the Micron Technology 
		Graduate Fellowship, and the Tau Beta Pi Graduate Fellowship.</p>
		<p>Dr. Bundy's research lab focuses on developing a technology for the rapid site-specific incorporation of 
		unnatural amino acids into proteins for orientation controlled biocatalyst immobilization. Dr. Bundy's lab also 
		works with the production of virus-like particles and the incorporation of non-natural amino acids into virus-like
		particles for site-specific bio-conjugation. This research has potential impacts in vaccine development, drug delivery 
		and nanostructure design with Sutro Biopharma currently licensing one of the patents resulting from Bundy’s research.</p>
		<p>Dr. Bundy strongly supports and encourages service-learning opportunities. You can download below instructions and posters 
		for 33 different hands-on demonstrations which use multiple senses to teach fundamental Heat & Mass Transfer principles. A basic 
		understanding of Heat & Mass Transfer is an essential for Engineering, Physics, and Chemistry careers. These demonstrations were 
		prepared by Dr. Bundy's ChE 376 Students and presented to hundreds of K-12 students as part of BYU's 2011 and 2012 Engineering Week.
		These demonstrations were prepared with the educator in mind and the cost and preparation time was kept to a minimum. Also one of these 
		demonstrations could be adapted into a science fair project.</p>
		
