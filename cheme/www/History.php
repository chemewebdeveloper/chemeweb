			<h2>History</h2>
			
			<p>Chemical Engineering at Brigham Young University was officially initiated in 1955 with 
			the renaming of the Department of Chemistry to the Department of Chemistry and Chemical Engineering.
			Only 1 full-time faculty member taught the chemical engineering courses at that time. In 1958, a
			separate Department of Chemical Engineering was formed, by this time with 3 faculty members. 
			The undergraduate program was first accredited in 1961 and has been accredited ever since. A 
			Masters degree in chemical engineering was approved in 1962, and the PhD program received approval
			in 1968. From those beginnings, the department has grown to its present size of 13 full-time faculty, 
			around 350 undergraduate students, and approximately 45 graduate students.</p>
			
