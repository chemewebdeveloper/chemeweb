	<p> Furthermore, the faculty generally learn the names of their students early in each semester, even in the freshman 
	class of 100, and work to foster an atmosphere of friendship and brotherhood. Excellence in teaching 
	is promoted, and student learning is enhanced by innovative methods, quality laboratories, and the 
	participation of undergraduates in faculty research programs (described below). As a result, the 
	undergraduate program, which is fully accredited by the Accreditation Board for Engineering and 
	Technology, is one of the finest in the United States.</p>
	<h2>Graduate Program</h2>
			<p>Graduate scholarship and research are conducted by all members of the faculty as part of the
			graduate program leading towards the M.S. and Ph.D. degrees. Research topics vary widely, 
			including such areas as biomedical engineering, catalysis, combustion, electrochemical
			engineering, energy, environmental engineering, teaching pedagogy, and thermodynamics.
			There have been many highlights in the scholarly work of the department, but a few of the "larger"
			events are worth mentioning. For example, under the direction of key Chemical Engineering faculty 
			at BYU, the Advanced Combustion Engineering Research Center (ACERC) was founded in 1985 by the National 
			Science Foundation (only 5 Engineering Research Centers were funded out of over 100 applications from 
			major universities). Over its first 10 years, this prestigious center brought in over 27 million dollars
			to BYU, resulted in 700 publications, and graduated 200 students. As another example, the Department of 
			Chemical Engineering at BYU was selected by the Design Institute for Physical Property Data (DIPPR), an 
			organization of the national American Institute of Chemical Engineers (AIChE), to manage and upgrade its 
			large database of thermophysical properties, beginning in 1998. Managed by three Chemical Engineering faculty
			members, the project serves chemical companies all over the world and brings in $250,000 per year.
			The quality of the department’s graduate program is very respectable. In 1995, the doctoral program in Chemical
			Engineering at BYU was ranked 50 out of 168 Chemical Engineering Departments in the United States by the National 
			Research Council. Furthermore, in that same year, the department was ranked #11 in the country in terms of federal 
			support of research and #20 in the country in terms of total research support. </p>
