			<h2> Undergraduate Program</h2>
			<p> Consistent with the emphasis of Brigham Young University, the Department of Chemical 
			Engineering is vitally concerned about the education of undergraduate students. Because of 
			that concern, every student is assigned an advisor immediately upon entering the department and 
			is required to visit with that advisor and review his/her progress toward graduation at least 
			twice during his/her academic career. In addition, the faculty of the department personally 
			teach all of the classes, as opposed to turning them over to Teaching Assistants (Teaching 
			Assistants help with the tutoring of students and with the grading of work, but they very rarely
			act as the main teacher for the class). Classes are relatively small, ranging from the largest 
			freshman class of approximately 100 students down to the smaller specialty classes of around 10-15, 
			thus helping to promote interaction among students and between students and faculty.</p>
			
