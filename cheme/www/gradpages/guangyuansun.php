					<h3>Research</h3>
							<!-- Insert paragraph here. Example: <p>INSERT PARAGRAPH</p> -->
							<p>An understanding of particle dynamics in turbulent flows is important to many biological, environmental and industrial fields. We are performing fundamental investigation into dispersive transport and time-temperature histories of Lagrangian particles in turbulent reacting flows. Of specific focus is the defeat of biological agents using explosively dispersed gas through thermal or chemical reaction. It is of great significance to predict the statistics of the interaction of biological aerosol with the reacting flow, particularly the full particle temperature probability density function (PDF). A computationally affordable stochastic modeling approach, one-dimensional turbulence (ODT), is a proven method that captures the full range of length and time scales (in one-dimension), and provides detailed statistical information of fine-scale
							turbulent-particle mixing and transport. Limited results of particle transport in ODT have been previously reported in non-reacting flow. Here we implement and validate a Lagrangian particle model into an existing reacting ODT code. The work investigates the functional dependence of the statistics of particle-flow interactions. Flow statistics of the ODT particle model are compared to both experimental measurements and DNS data. The successful validation of Lagrangian particle transport in ODT has the potential for much more efficient investigation of fine-scale turbulence-particle interactions and access to a wider Reynolds number range than available using DNS.</p>
				
