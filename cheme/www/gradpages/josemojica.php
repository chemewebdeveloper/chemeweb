					<h3>Research</h3>
							<!-- Insert paragraph here. Example: <p>INSERT PARAGRAPH</p> -->
							<p>The efficient and clean production of energy is a goal most nations are interested in achieving. The economic and environmental benefits of more efficient and cleaner energy production systems are making these technologies more attractive to businesses, institutions, and consumers. Deciding when to invest in such technologies is a challenge by itself because of the initial investment costs and the uncertainty in key variables that can make the project profitable or not. A key part of my research is the optimization of investment decision of combined heat and power plants (CHP) under uncertainty.
							Another area of involvement is in creating models and novel algorithms for Model Predictive Control and optimization of energy generating systems such as CHP and solar power plants. The optimization is targeted at better integrating energy storage, demand and weather forecasts, and economic constraints to increase profitability and energy conservation.</p>
				
