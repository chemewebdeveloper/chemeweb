					<h3>Research</h3>
							<!-- Insert paragraph here. Example: <p>INSERT PARAGRAPH</p> -->
							<p>The focus of my research is spontaneous ignition of pure compounds. My project can reasonably be divided into three segments: autoignition temperature (AIT) measurement, prediction, and analysis. ASTM standard E659 specifies an apparatus and procedure for AIT measurement, however much of the available data were measured using a different or previous standard. This results in highly inconsistent measured AIT values. Because of that, a significant portion of my project is to analyze the data that is currently available in the literature and try to distil the most consistent, reliable data. Additionally, I will augment the available data by measuring AIT for several compounds using ASTM standard E659. Finally, when I have finished my analysis of the data, I will use the values that seem most reliable to develop a prediction method for those compounds which have not been measured. This prediction method will likely be based on structural group contributions, but may include other descriptors, such as the energy required to break the weakest carbon-carbon bond, or the average and longest carbon chain length.</p>
				
