							<p>I'm currently researching calcium carbonate precipitation kinetics. When two separate solutions containing calcium ions and carbonate ions are mixed, an over saturated solution is formed. The solution forms precipitates of solid calcium carbonate particles. It is commonly believed that kinetics of the system dictate which of several metastable forms of solid calcium carbonate are formed however little is known about the kinetics of this dynamic colloidal system. A kinetic model is underdevelopment for this system.
							Additionally, it is of key interest to study the effects of mixing on this system. The One-Dimensional turbulence model (ODT) is a computational solution for conducting such studies. ODT uses affordable and effective means to resolve fine scale turbulent structures using a stochastic eddy approach.</p>
				
