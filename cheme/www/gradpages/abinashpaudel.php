					<h3>Research</h3>
							<!-- Insert paragraph here. Example: <p>INSERT PARAGRAPH</p> -->
							<p>My research pertains to applying one dimensional turbulence to model fire spread through biomass fuel beds. As wildfires are hazardous and destroy tremendous amount of woodlands and increase the risk of flood, landslides, pollution etc, it is of utmost importance to model these fires and understand their behaviors to control them in timely manner. Since these fires depend on number of factors like fuel type, environmental condition and modes of heat transfer, it is difficult to model flame propagation especially turbulent flames. The objectives of my research project is to:
							i) develop a particle combustion model in ODT to understand description of the flame structure at the interface between flame and unburnt fuel.
							ii) use ODT to simulate flame propagation in biomass fuel beds to quantify the particle properties on the spread.
							iii) investigate particle heat-up and understand the fuel propagation, the effects of turbulence, the feedback to the gaseous medium from the particle combustion, and the important statistics that describe a flame front.</p>
				
