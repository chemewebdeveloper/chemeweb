					<h3>Research</h3>
							<!-- Insert paragraph here. Example: <p>INSERT PARAGRAPH</p> -->
							<p>With the process of cryogenic CO2 separation, a novel energy storage system can be integrated by using an open loop natural gas refrigeration system. In this system energy is stored during non-peak times by liquefying natural gas. During peak demand when energy is at a premium, the liquefaction of natural gas will terminate and the stored liquefied natural gas will be used to carry out the cryogenic separation process relieving the parasitic load on the power plant. Once the natural gas has been vaporized and warmed, it is combusted in a simple natural gas turbine increasing power output of the power plant during peak demand. Flue gas from the turbine is then be combined in the coal boiler to provide an effective combined cycle system and ensure CO2 removal from both natural gas and coal combustion products. This is a retrofit, bolt-on system that allows existing coal-fired power plants to reduce CO2 emissions by up to 99%, increases peak load capacity thru added natural gas combustion, and increases load following ability by introducing a large intermittent refrigeration system and natural gas combustion cycle that can be quickly controlled to produce or consume energy as needed.</p>
				
