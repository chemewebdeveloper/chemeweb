					<h3>Research</h3>
							<!-- Insert paragraph here. Example: <p>INSERT PARAGRAPH</p> -->
							<p> A growing world population and increased modernization in developing countries place an ever-increasing demand for energy. The solution to meeting the energy needs of the future will most likely come from a combination of energy sources, two of which are biomass and petroleum coke. One way that biomass and petroleum coke can be transformed into useful products such as energy and chemicals is through gasification, which converts any hydrocarbon to a mixture of hydrogen and carbon monoxide termed synthesis gas.
							Many complex processes are involved in the gasification of a carbonaceous fuel such as drying, pyrolysis, and char gasification. Although the drying and pyrolysis of most fuels is nearly instantaneous at the high temperatures characteristic of most commercials gasifiers, the subsequent gasification of the char is slow and is therefore rate-controlling. A better understanding of the gasification reactivity of different fuels enables the prediction of residence time and temperature requirements for complete reaction in highly thermal-efficient processes such as Integrated Gasification Combined Cycle (IGCC) to generate power.
							In my research, we work with GE to gain a better understanding of biomass gasification with a particular focus on char gasification kinetics of sawdust, switchgrass, corn stover, and petroleum coke. We measure and model the CO2 char gasification rates of these fuels in BYU’s high-pressure flat-flame burner at industrial-like conditions of high pressure and temperature for particles that have been pyrolyzed at rapid initial particle heating rates. The gasification rates in these life-like industrial conditions will provide meaningful data to the design and efficient operation of commercial entrained-flow gasifiers.</p>
				
