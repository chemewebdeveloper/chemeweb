					<h3>Research</h3>
							<!-- Insert paragraph here. Example: <p>INSERT PARAGRAPH</p> -->
							<p>DNA microarrays are chip-based, analysis tools which can perform hundreds of thousands of assays in parallel. They can be used identify genes or gene expression levels present in a sample and are a key technology in genomic sciences and emergent medical techniques. Despite their abundant use in research laboratories, microarrays have not been used in the clinical setting to their fullest potential due to the difficulty of obtaining reproducible results.</p>

							<p>Using a parameterized coarse-grain model of DNA, we study the thermodynamics of hybridization on surfaces for simple probe/target pairings. One part of our work is obtaining free-energy projections of the mechanism of DNA hybridization from two-dimensional umbrella sampling, each requiring over 11,000 simulations.</p>

							<p>We also research how factors found in real microarrays affect the hybridization process. Specifically, we show how the presence of multiple probes on the surface affects hybridization, and examine how the number and location of mismatched base pairs affect the stability of the duplex. We are also researching how the location of probe surface connections influences the hybridization process. This research gives the most complete picture to date of the biophysics involved in microarray performance and how this knowledge can be used to improve next-generation devices.</p>
				
