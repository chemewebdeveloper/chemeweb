					<h3>Research</h3>
							<!-- Insert paragraph here. Example: <p>INSERT PARAGRAPH</p> -->
							<p>My research focuses on making on nano electrical components from DNA templates. My work especially focuses on depositing different metal components at specific locations of a DNA template which is integral for making electrical components like transistors. In addition, I also worked on making metal and semiconductor nanowires. We were able to make nickel and copper wires as small as 30 nm in width and then displace them with semiconductor materials. These work represent important progress in this field and the works are in the process of publication.</p>
				
