					<h3>Research</h3>
							<!-- Insert paragraph here. Example: <p>INSERT PARAGRAPH</p> -->
							<p>Battery performance directly relates to the selected electrode materials as well as the corresponding electrode microstructure and resulting transport processes. My research, as a master’s student, focuses on relating these transport processes and thus battery performance to their physical origins (i.e. electrode materials and microstructure). Specifically, it investigates the effect of different carbon additives on the microstructure and performance of battery cathodes. Imaging and direct measurement of electronic and ionic conductivities are used to understand the effect of different carbon morphologies and particles sizes on two battery chemistries: primary alkaline batteries and rechargeable polymer batteries (TEMPO-methacrylate).
							The measurement of conductivities for TEMPO-methacrylate cathodes is one of the first of its kind because prior research primarily focused on bulk performance metrics (i.e. voltage, capacity, and cycling behavior) rather than specific transport performance metrics. While transport measurements have been previously performed for alkaline battery cathodes, this work adds to the discussion by providing analysis of different carbon morphologies as well as particle sizes. Ultimately, this research identifies the basic trends of electronic and ionic conductivities as function of carbon morphology and particle size—linking material properties to battery performance, and paving the way for improved cathode design.</p>
				
