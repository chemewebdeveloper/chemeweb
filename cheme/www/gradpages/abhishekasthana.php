					<h3>Research</h3>
							<!-- Insert paragraph here. Example: <p>INSERT PARAGRAPH</p> -->
							<p>My PhD work demonstrates the application of molecular simulations (MD) in two different areas: proton transport in bulk water and estimation of the dipole moment of polar organic compounds. In both areas, relatively few 		       					successful and robust methodologies exist.
							In the first part, a new polarizable water model is developed for MD simulations of the proton transport process. The model was parametrized from a combination of quantum chemical calculations and experimental water properties. The model was implemented in MD simulation studies of liquid water at room temperature, as well as with excess protons. For pure water the model gave good agreement with experimental properties. The proton transport rate for a single excess proton also gave a good match with the experimental value. The water model was further extended to include chloride ions. At 0.2 M concentration the resulting density and structure agreed well with experiment, and the proton transport rate was found to be slightly reduced. The model was further extended to include multiple excess protons.
							For the second part of the project, an open source ab initio MD program, SIESTA, was used to perform simulations of several organic compounds which potentially have multiple stable conformations, to determine their average dipole moments. A series of methods was developed. The most robust method involved modifications to the SIESTA code and statistical analysis of the resulting configurations, in order to more accurately predict the average dipole moment. The resulting dipole moments were in good agreement with the experimental values for cases in which experimental values were reliable. Based on this study, a general method to estimate the average dipole moment of any compound is proposed.</p>
				
