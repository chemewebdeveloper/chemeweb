					<h3>Research</h3>
							<!-- Insert paragraph here. Example: <p>INSERT PARAGRAPH</p> -->
							<p>I work with turning biomass into biofuel through a process called sygas fermentation. We use a Clostridium bacteria that converts gasified biomass H2, CO, and CO2 (syngas) into alcohols. One issue with this type of biofuel production is the ability to supply low soluble gasses like H2 and CO into liquid containing bacterial cells. I work with modeling and enhancing the mass transfer of these low soluble gasses. Much of my research has focused on hollow fiber reactors in order to accomplish this. We have found that the hollow fiber reactor has much better mass transfer characteristics than other common reactors.
							Another issue with syngas fermentation is the ability to provide an adequate amount of electrons in order to reduce CO2. In response to this issue, I also work with using bioelectric reactors (BER) that supply external electrons with electron mediators in order to enhance electron usage. Many of these electron mediators can be toxic to cells in certain redox states. Thus, I am analyzing the toxicity of a common mediator in its several redox states. In addition, I am performing a thermodynamic analysis on this electron mediator in order to more correctly model its electron transferring capabilities.</p>
