					<h3>Research</h3>
							<!-- Insert paragraph here. Example: <p>INSERT PARAGRAPH</p> -->
							<p>To better study the live fuel combustion behavior and develop the current semi-empirical bush combustion model, a laminar flow flat-flame burner was used to provide a convection heating source to ignite individual live fuel samples. In this research project, four Utah species were studied: Gambel oak (Quercus gambelii), canyon maple (Acer grandidentatum), big sagebrush (Artemisia tridentata) and Utah juniper (Juniperus osteosperma). Leaf geometrical parameters and time-dependent combustion behavior were recorded. Qualitative results included various combustion phenomena like bursting, brand formation and bending. Quantitative results included determination of best correlations for (a) leaf geometrical properties (individual leaf dry mass (mdry), thickness (Î”x), leaf width (W) and leaf length (L)) and (b) combustion characteristics (e.g., time to ignition (tig), time of flame duration (tfd), time to maximum flame height (tfh), time to burnout (tbrn), and maximum flame height (hf,max)).
							A semi-empirical bush model was expanded to describe the combustion behavior of the three Utah species (Gambel oak, canyon maple and Utah juniper). Leaf placement and bush structure were determined from the statistical model. A new flame area simulation was explored in the semi-empirical bush model in order to improve the bush burning predictions.</p>
				
