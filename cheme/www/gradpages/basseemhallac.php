					<h3>Research</h3>
							<!-- Insert paragraph here. Example: <p>INSERT PARAGRAPH</p> -->
							<p>Water-gas shift (WGS) is a critical reaction step that plays role in hydrogen production from steam methane reforming (SMR) and coal gasification, and in liquid hydrocarbon production from Fischer-Tropsch synthesis. It controls the CO:H2 ratio according to the following reversible chemical reaction:
							CO + H2O ---> CO2 + H2
							In the lab, we are studying the functional and structural roles of lanthana in industrial iron-based WGS and fischer-tropsch catalysts. The catalysts are characterized using BET surface area, X-ray diffraction, electron dispersive spectroscopy (EDS), scanning electron microscopy (SEM), H2 chemisortption, and temperature-programmed reduction (TPR). The kinetics and stability of the catalysts are also studied. Furthermore, catalytic extents of reduction are correlated to UV-visible spectra via Kubelka-Munk function.
							Results have shown that minor addition of lanthana (0.5 wt%) to the WGS catalysts enhance their activity and stability, while further loadings impose a negative effect. Lanthana is playing an important role in thermally stabilizing the catalysts as well as facilitating the CO adsorption and oxidation on the catalyst surface.</p>
				
