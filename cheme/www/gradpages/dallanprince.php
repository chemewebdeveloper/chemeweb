					<h3>Research</h3>
							<!-- Insert paragraph here. Example: <p>INSERT PARAGRAPH</p> -->
							<p>The wide range of scales involved in wildland fires is a modeling challenge, since empirical models generally need more physics and physical models require excessive amounts of computer time. Additionally, live fuels burn differently than dead fuels of similar moisture contents. I am working to understand and model the behavior of live fuels and to bridge the gap between full physical simulations and empirical correlations. Operational models are most often limited primarily by an oversimplification of the fuel matrix. In our model, we describe the specific location of all the fuel down to the leaf-scale. We model fire propagation through a shrub based on flame-fuel overlap. Flames from burning leaves engulf other leaves, which ignite according to experimentally correlated preheat times. Overlapping flames are enlarged according to flame interaction algorithms we’re developing. The description of fuel locations and flame boundaries allows a specific treatment of heat transfer. Maintaining simplicity in other aspects of the modeling maintains computational speed. </p>
				
