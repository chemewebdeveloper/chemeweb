							<p>Traditional sources of power generation are increasingly mixed with a growing fraction of emerging energy sources such as wind and solar. Wind and solar have the characteristic of being intermittently available in the case of cloud cover or as weather patterns move through an area. As non-traditional sources of energy integrate into the base-load, there is a critical interplay to improve load-following to allow full utilization of the intermittent sources on a smart grid. The contribution of this study is to investigate the application of smart grid forecasting, grid abnormal situation management, and integration with cryogenic carbon capture to fully utilize intermittent power sources.</p>
				
