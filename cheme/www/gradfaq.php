<p>
	<em><strong>Q</strong>: </em><em>Do I need to take the GRE exam?</em><a name="Q1"></a></p>
<p class="indent">
	<strong>A</strong>: All applicants must take the general GRE exam within 4 years prior to the application and submit an official score to the BYU graduate school.</p>
<hr />
<p>
	<em><strong>Q</strong>: Do I need to take any topical GRE exams, such as engineering or chemistry?</em><a name="Q2"></a></p>
<p class="indent">
	<strong>A</strong>: No.</p>
<hr />
<p>
	<em><strong>Q</strong>: What is the minimum GRE exam score?</em><a name="Q3"></a></p>
<p class="indent">
	<strong>A</strong>: There is no minimum; however, to be competitive an applicant should obtain scores of at least 155 Quantitative and 153 Verbal (new 130-170 scale) and 3.0 Analytical.&nbsp; We generally recommend that students with a score below any of these levels spend additional preparation time and retake the GRE exam.</p>
<hr />
<p>
	<em><strong>Q</strong>: Is the <span data-scayt_word="TOEFL" data-scaytid="3">TOEFL</span> exam required for admission?</em><a name="Q4"></a></p>
<p class="indent">
	<strong>A</strong>: The Department follows the <a href="http://www.byu.edu/gradstudies/admissions/englishtest.php" target="_blank">English proficiency requirements</a> of the University.&nbsp; In particular, applicants who have not received a university degree from an English-speaking country are required to submit official <strong><span data-scayt_word="TOEFL" data-scaytid="5">TOEFL</span></strong> or <strong><span data-scayt_word="IELTS" data-scaytid="7">IELTS</span> </strong>scores. Minimum scores are listed at the above web page.&nbsp; The test must have been taken within the previous two years. A students whose native tongue is English, but who is from a non-English speaking country, may get this exam requirement waived by speaking (by phone) with a faculty member at BYU.</p>
<hr />
<p>
	<em><strong>Q</strong>: Can I send unofficial copies of my GRE and <span data-scayt_word="TOEFL" data-scaytid="8">TOEFL</span> documents?</em><a name="Q5"></a></p>
<p class="indent">
	<strong>A</strong>: The Chemical Engineering Department will accept electronic copies of these documents for preliminary evaluation purposes, but the University will not admit a student unless official documents are received.</p>
<hr />
<p>
	<em><strong>Q</strong>: How much does it cost to attend BYU?</em><a name="Q6"></a></p>
<p class="indent">
	<strong>A</strong>: Estimated costs for graduate students are available at <b><a href="http://www.byu.edu/gradstudies/admissions/costsfinaid.php" target="_blank">The Graduate Studies web site</a></b>.&nbsp; For a single graduate student, a typical cost of living is $13,500 per year in addition to any tuition.&nbsp; Nearly all graduate students in our program receive substantial financial aid that meets or exceeds these estimated costs.</p>
<hr />
<p>
	<em><strong>Q</strong>: What financial assistance is available?</em><a name="Q7"></a></p>
<p class="indent">
	<strong>A</strong>: Nearly every graduate student receives financial aid in the form of a stipend or salary, and in many cases tuition support.&nbsp; For the 2012-2013 school year we anticipate stipend offers of $23,000 for new Ph.D. students and $22,000 for new M.S. students.&nbsp; The department bears a large portion of the cost of first-year graduate students and requires service as a teaching assistant.&nbsp; Thereafter stipends become the responsibility of the student&#39;s research advisor in the form of a research assistantship.&nbsp; Currently the Chemical Engineering Department pays full tuition for first-year Ph.D. candidates.&nbsp; After the first year, Ph.D. students are typically able to get additional tuition support.&nbsp;</p>
<p>
	We encourage applicants and beginning graduate students to apply for <a href="http://chemicalengineering.byu.edu/system/files/national-graduate-fellowships_.html">competitive fellowships</a> provided by private foundations and U.S. government agencies such as the National Science Foundation and Department of Defense.&nbsp; These lead to significant prestige and monetary benefit.</p>
<hr />
<p>
	<em><strong>Q</strong>: Do I need to be a member of the Church of Jesus Christ of Latter-day Saints to attend BYU?</em><a name="Q8"></a></p>
<p class="indent">
	<strong>A</strong>: No. Admission to the University and to the Department is done without regard to religious beliefs. A large fraction of our graduate students are not members of the Church.&nbsp; Many students choose to attend BYU over academically comparable programs because of the BYU <a href="http://saas.byu.edu/catalog/2009-2010ucat/GeneralInfo/HonorCode.php" target="_blank">Honor Code</a> and the conservative and wholesome social atmosphere.&nbsp; Under the Honor Code, all students who attend Brigham Young University must be honest, obey the law, live in separate housing from the opposite sex if unmarried, be modest in dress, and abstain from tobacco, alcohol, coffee, and illegal or harmful drugs.</p>
<hr />
<p>
	<em><strong>Q:</strong> How long does it take to finish a degree?</em><a name="Q9"></a></p>
<p>
	<strong>A:</strong> The M.S. and Ph.D. degrees each require coursework as well as an independent research project leading to a written thesis or dissertation.&nbsp; A student with a bachelor of science (B.S.) degree may apply to either program, that is, a Ph.D. degree may be earned without first obtaining an M.S. degree.&nbsp; For a student with a B.S. degree the typical time required to complete a graduate degree is 2 years for an M.S. or 4.5 years for a Ph.D.&nbsp; Faculty research advisors are committed to helping their students graduate as quickly as possible, but ultimately the the student&#39;s work ethic and research productivity determine the time required.</p>
<hr />
<p>
	<em><strong>Q</strong>: What are the course work and research requirements for a Ph.D. or M.S. in Chemical Engineering?</em><a name="Q10"></a></p>
<p class="indent">
	<strong>A</strong>: The details are given in the <b><a href="http://chemicalengineering.byu.edu/graduate-handbook">Graduate Handbook</a></b>.</p>
<hr />
<p>
	<em><strong>Q</strong>: How do I improve my chances of admission?</em><a name="Q11"></a></p>
<p class="indent">
	<strong>A</strong>: Admission is made by looking at a number of factors including grades in technical classes, GRE scores, letters of recommendation, prior experience, and area of research. Probably the easiest way to improve one&#39;s chances of admission is to study for the GRE exam to improve one&#39;s individual GRE scores. For international students, we prefer applicants to the PhD program over the MS program.</p>
<hr />
<p>
	<em><strong>Q</strong>: Where can I get information on BYU&#39;s graduate program in general?</em><a name="Q12"></a></p>
<p class="indent">
	<strong>A</strong>: This information is provided by the <b><a href="http://www.byu.edu/gradstudies/">Office of Graduate Studies</a>.</b></p>
