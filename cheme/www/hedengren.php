		 <p>Dr. Hedengren's current research activities are to develop efficient modeling 
		 and optimization methods for large-scale dynamic systems. Combining first 
		 principles with data driven models allows complex systems to be analyzed to gain
		  fundamental process insight. This insight can be used to design, monitor, and 
		  optimize a variety of systems including biological, energy, environmental, and
		   safety applications. Process Research and Intelligent Systems Modeling (PRISM)
		    is developing a world class collaborative research group for the application 
		    of innovative advanced process control and optimization techniques.</p>
		    <hr />
		 <p>He earned his B.S. and M.S.
		  in Chemical Engineering from Brigham Young University in 2002 and Ph.D. from 
		  the University of Texas at Austin in 2005. For his M.S. degree he worked with 
		  Merrill Beckstead on the C-SAFE (ASCI) project, improving rocket propellant 
		  combustion simulations. While at the University of Texas at Austin, he worked
		   with Tom Edgar to develop innovative numerical techniques for optimization of
		    large-scale models in process control. </p>
		<p>After completing his Ph.D. studies in 2005, he worked with PAS, Inc. as a 
		consultant for SABIC Ibn Zahr, TOTAL Petrochemical, SASOL South Africa, Celanese,
		 and DuPont in advanced process control and optimization. In 2007 he joined ExxonMobil
		  Chemical to develop solutions that control and assess quality of chemicals manufacture 
		  and improve the safety and reliability of operations. </p>
		  <p>Dr. Hedengren's current research activities are to: </p>
