<p>
	The Chemical Engineering Department offers a professional program leading to the bachelor of science degree. The first two years of this program are considered to be <em>pre-professional</em> and permit unrestricted enrollment for any student who qualifies for admission to the university. The remaining two years are considered to constitute the <em>professional program</em>.</p>
<p>
	Any student who is admitted to the university may choose this program as a possible major. All students are urged to declare their major upon first entry to the university or as soon thereafter as possible by contacting the college advisement center (242 CB).</p>
<p>
	Students electing to major in this program must successfully complete the minimum pre professional program requirements that are detailed below before applying for acceptance into the department professional program.</p>
<p>
	Admission to the professional program is available to all students in good academic standing with the university who have:</p>
<ul>
	<li>
		passed the prerequisite courses for the first semester professional courses, namely ChEn 273 and Math 302</li>
</ul>
<ul>
	<li>
		submitted to the department a completed Application for the Chemical Engineering Professional Program.</li>
</ul>
<p>
	The Application for the Chemical Engineering Professional Program requires the student to meet with his/her department advisor for direction and counseling concerning performance in the pre-professional program classes and successful completion of the professional program.</p>
<p>
	With the exception of ChEn 378 and ChEn 310, none of the required 300 or 400 level courses in chemical engineering may be taken before acceptance into the professional program.</p>
<p>
	&nbsp;</p>
<p>
	<strong>1. Application Procedures</strong>:<br />
	<br />
	Applications for the professional program are due each April 15, but may be submitted any time prior to that date. All students who will meet the professional program requirements by Fall semester should submit the application by April 15 even if completion a prerequisite class will not occur until Spring Term. Application forms are available at the department office. The application form includes a tentative plan for graduation and major elective classes. The student completes these parts of the application and then meets with his/her advisor in advance of the April 15 deadline. All completed applications must include the advisor&rsquo;s signature.</p>
<p>
	Transfer students should submit an application to the department for admission to the professional program concurrently with their application to the university. Transfer student applications do not require meeting with an advsior or an advisor&rsquo;s signature. However, transfer students should meet with their advisor shortly after arriving on campus.</p>
<p>
	&nbsp;</p>
<p>
	<strong>2. Professional Program Advising:<br />
	</strong><br />
	There is a strong correlation between successful completion of the professional program and student performance in &quot;key performance indicator courses&quot; in the pre-professional program. Key performance indicator courses are shown in shaded boxes on the flow charts.</p>
<p>
	The key performance indicator courses are:<br />
	Math 112, 113, 302, 303<br />
	Chem 111, 112 (or 105, 106, 107); 351, 352<br />
	ChEn 170, 263, 273<br />
	Phscs 121, 220<br />
