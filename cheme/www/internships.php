<p>Internships and co-ops consist of working for a company, generally full-time and while not taking classes, before completing your degree. All students are highly encouraged to do internships or co-ops because they provide real engineering experience, which benefits your education and future employment prospects. Internships are usually done during the summer months and do not delay the date of graduation. Co-ops usually entail multiple work periods and may extend the time required to graduate.  However, this delay should not be considered unfavorably for multiple reasons:</p>
<ol>
	<li>Students are paid very well for their time (about 50% of starting engineer salary). </li>
	<li>Student who perform well in a co-op generally have an advantage in securing full-time employment upon graduation.</li>
	<li>Delaying graduation by one year is not significant when compared to a 40-year career.</li>
</ol></p>

<p>Because co-op periods may displace normal Fall and Winter semesters at the university, carefully planning is required to accommodate the interruption.  <a href="http://chemicalengineering.byu.edu/Co-op Schedules.pdf">Here is a list of possible co-op schedules</a>.</p> <br>

<p>If you are wondering how to get an internship or co-op, <a href="http://sot.et.byu.edu/advisement/internships">the college internship website has helpful advice for you</a>.</p> <br> 

<p><b>Academic credit (ChEn 199R)</b> can be awarded for fulfilling an internship or a co-op, but must be arranged prior to the start of the internship or co-op.  To do so, <a href="http://chemicalengineering.byu.edu/InternshipChemEng.pdf">download and print this form</a>, then follow its instructions. As described, if the company you plan to do an internship with does not have a "master agreement" with BYU, you are responsible to <a href="http://chemicalengineering.byu.edu/InternshipMA.pdf">download it</a> and have the company complete it and return it to the college internship office. ChEn 199R credit can count as an engineering elective and fulfills the innovation graduation requirement. You may see Dr. Wheeler if you have additional questions.</p>
