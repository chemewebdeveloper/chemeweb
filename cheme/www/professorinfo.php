 <img src="images/directory/bartholomew.jpg" /><p>Dr. Bartholomew joined the Chemical Engineering faculty in 1973. He 
 teaches courses treating kinetics, catalysis, catalyst deactivation, air pollution control, creativity, and 
 engineering materials. Prior to joining Brigham Young University, he was employed by Corning Glass Works, 
 Corning New York, where he served as Senior Chemical Engineer in Automotive Emissions Control and Surface 
 Chemistry Research. He is a member of the American Chemical Society (past-president of the Central Utah Section), 
 the American Institute of Chemical Engineers, the California Catalysis Society (past-president), The Rocky Mountain 
 Fuel Society (founder), Tau Beta Pi, Sigma Xi, and the Western States Catalysis Club (founder). He was a 1983 recipient 
 of the BYU Karl G. Maeser Research Award and was chosen as the Outstanding Faculty member in the Engineering College in
 1985.</p>
 


 


