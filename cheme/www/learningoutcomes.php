<p>
	Student/Learning Outcomes which we would like our students to possess when they leave the university and enter the workforce are subdivided into <a href="courseobjectives.php">competencies</a>. To ensure that students acheive these compentencies, and to continually identify areas for improvement, the department assesses student performance in each competency every semester. &nbsp;This is done using direct methods by the faculty and indirect methods by the students. &nbsp;The data collected every semester is discussed in the department undergraduate committee and recommendations for improvements are given to each instructor. &nbsp;To &quot;close-the-loop&quot; and ensure improvements are being made, instructors are required to explain in writing how they addressed the recommendations given by the committee the next time the course is taught.</p>
