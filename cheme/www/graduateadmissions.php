<h2>
	<a name="A_financial_aid"></a>Financial Aid and Choosing an Advisor</h2>
<p>
	Every student in the chemical engineering graduate program receives financial aid in the form of a stipend (salary) and in many cases tuition support.&nbsp; This does not require a separate application.&nbsp; Each student accepted into the program will have his or her financial aid package specified in the offer letter.</p>
<p>
	Currently, the department pays full tuition for first-year Ph.D. students and partial tuition for second-year Ph.D. students.&nbsp; Most Ph.D. students take the bulk of their classes in the first two years and take fewer credits in later years.&nbsp; While the department does not guarantee tuition support after the first year, it is often available from faculty advisors through their research grants.&nbsp; The department does not currently provide tuition support for M.S. students.&nbsp; Tuition amounts can be found on the <a href="http://www.byu.edu/gradstudies/admissions/costsfinaid.php">Graduate Studies webpage</a>.</p>
<p>
	Nearly every student receives a stipend through a combination of research and teaching assistantships.&nbsp; The yearly stipend for students in 2011-2012 is $23,000 for Ph.D. students and $22,000 for M.S. students.&nbsp; These amounts are sufficient that most students can avoid accumulating debt in the form of student loans.&nbsp;</p>
<p>
	Choosing a faculty research advisor is one of the most important decisions for graduate students.&nbsp; In our program this decision is made <em>after</em> a student begins the program and after he or she has been able to interview at least six faculty members as potential advisors.&nbsp; This policy is designed to allow sufficient freedom for the best match between students and faculty.&nbsp; We encourage students to correspond with potential advisors before applying to and attending BYU.&nbsp; However, no binding commitments with respect to advisor can be made until after the student begins the graduate program.</p>
