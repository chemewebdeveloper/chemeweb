<h2>
	&nbsp;<a name="A_requirements"></a>Application Requirements </h2>
<p>
	The Graduate Record Examination <a href="http://www.gre.org/">(GRE)</a> is required for all applicants. Applicants whose native language is not English are also required to take an English proficiency exam (such as TOEFL) as described on the <a href="http://www.byu.edu/gradstudies/admissions/englishtest.php" target="_blank">BYU Graduate Studies web page.</a>&nbsp;&nbsp;Average GRE scores from admitted students over the past five years, along with other program statistics are available at the BYU Graduate Studies web pages for our <a href="http://www.byu.edu/gradstudies/programs/formdetails.php?program=41&amp;submit=select" target="_blank">PhD</a> and <a href="http://www.byu.edu/gradstudies/programs/formdetails.php?program=42&amp;submit=select" target="_blank">MS</a> programs.</p>
<p>
	All applicants must have a bachelor of science (B.S.) degree in chemical engineering, in another field of engineering, or in a physical science.&nbsp; Students in the final year of their B.S. program who will complete the degree before their graduate school entrance date are included in this group.&nbsp; The most competitive applicants will have a chemical engineering B.S. with a minimum 3.0 GPA (4.0 scale) in upper-division chemical engineering courses and a minimum overall 3.3 GPA.</p>
<p>
	Applicants with a technical, non-chemical engineering background will be considered and are encouraged to apply.&nbsp; Students in these areas who are admitted to the program are generally required to take supplemental chemical engineering coursework during the first year of graduate school on a case-by-case basis. Such students may also be admitted on a provisional degree-seeking basis, in which their status is changed to regular degree-seeking once necessary background courses have been taken.</p>
