					<h2>Transport in Contact Lenses</h2><hr /><br />

			
							<img src="images/Department/transportincontactlenses.jpg">
					


						<p>Dr. Pitt has been studying theoretical aspects of the transport
						 of oxygen and ions in soft contact lenses since 2006. Recently he 
						 and his students have conducting experiments on the transport of 
						 phospholipids in silicone hydrogel contact lenses, with the aim of
						  using the contact lens as a depot to slowly release phospholipids to
						   the tears bathing the cornea. This technology could be used to increase
						    the comfort of wearing contact lenses, particularly by people who lack
						     adequate phospholipids, and thus who often have dry or itching eyes.</p>

