					<h2>Ultrasonic Activated Drug and Gene Delivery</h2><hr /><br />

			
							<img src="images/Department/ultrasonic.jpg">
				
						<p>Over the past decade we have found that anti-cancer drugs can
						 be sequestered inside small carrier vesicles that can be injected 
						 into the blood stream and travel throughout the body within the 
						 circulatory system, and then be released from the carrier at a 
						 precise location and timing using ultrasonic irradiation. The drug 
						 carriers keep the drug from being released prematurely and 
						 interacting with healthy tissues, resulting in side effects.
						  We are actively studying this method of drug delivery, and 
						  are designing drug carriers.</p>
				
