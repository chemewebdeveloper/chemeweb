			<p>Each year the American Institute of Chemical Engineers (AIChE) sponsors 
			a student paper competition. At regional AIChE meetings, students from 8 to
			 10 universities present papers that they have written, and the winners of 
			 those regional competitions then compete at the national AIChE meeting
			  where a national winner is selected. </p>
            <p>Chemical engineering students from BYU compete very strongly in these 
            student paper contests. They almost always win first, second, or third 
            place (often multiple BYU students win) at the regional level. Moreover,
             on two occasions (1993 and 2000), a chemical engineering student from BYU
              was the national winner of the AIChE student paper competition.</p>
            
	