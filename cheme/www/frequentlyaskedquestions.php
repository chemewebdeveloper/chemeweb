    <h2><a name="transfer">What classes should I sign up for as a new transfer student?</a></h2>
   	 <p>If you have one or more semesters of college credit (not just AP credit) and you 
         are being admitted to BYU as a transfer student, then you probably wonder what classes
         to take your first semester or two at BYU. Actually, to answer that question it may be 
         necessary to plan out additional semesters. Some of your academic credit from 
         courses taken at your previous university will satisfy major and general
         requirements at BYU, creating a very customized schedule for your individual 
         case. Furthermore, chemical engineering is a busy program with less flexibility
         than you might imagine, and every semester affects the others. So, not taking 
         a critical course in a particular semester may end up delaying your graduation.</p>
   	 <p>Before you register for classes your first semester at BYU should should seek 
         the advice of the transfer credit coordinator, who will also become your academic advisor
         for the remainder of your degree program at BYU. This faculty member will help you customize 
         your schedule to meet your needs and optimize your progress toward graduation. 
         For additional information, please see the <a href="transferstudents.php">transfer students</a> page.</p>



