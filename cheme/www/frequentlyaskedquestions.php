  <h2><a name="apcourses">How do I know if AP courses I have taken are given credit at BYU?</a></h2>
    <p>Many incoming students have AP credit for Math, Chemistry, English, 
    and/or Physics. The University materials that you received should have 
    told you the specific BYU courses for which you have been given credit. 
    You can also look at the <a href="http://saas.byu.edu/tools/b4byu/sites/b4/?new-freshman/apib-guides/">
    BYU website explaining AP credit</a>. Initial questions 
    regarding AP credit should be directed to the <a href="http://www.et.byu.edu/college-advisement-center-0">
    Advisement Center</a> for the College of Engineering. Having AP credit
     in required classes allows students to take 
     other classes normally not taken until later, thereby reducing the otherwise-heavy
      course loads in later semesters. Your advisor can help you decide which classes
       to take early.</p>
