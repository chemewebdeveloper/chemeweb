			 <h2><a name="howmanycredits">How many credits should I take per semester?</a></h2>
	 	<p>A credit load of 14-16 credit hours is generally a healthy
	 	 full-time academic load for a semester, and we recommend not 
	 	 exceeding those numbers. (Note: for Spring or Summer terms,
	 	  which compress a semester into half the time, 7-8 credits is a
	 	   recommended maximum.) Further, you will learn that technical 
	 	   courses (e.g. science, math, and engineering) require more work 
	 	   and time than many other courses, so the full-time loads recommended 
	 	   above should usually include at least one non-technical class per semester.</p>
<h2><a name="job">Is a part-time job really a bad idea?</a></h2>
	A full academic load in chemical engineering requires a great deal of your time 
	and effort. In fact, when considering the time for classes, studies, assignments,
	 and the daily activities of life (sleep, hygiene, meals, exercise, and spiritual 
	 food), it's a full-time life! Depending on the number of credit hours that you take
	  in a semester, the remaining "free" time available for such activities as a job are
	   very limited (usually less than 10 hours/week).  If your financial situation 
	   requires you to work, you may need to reduce your academic load accordingly.
	    The following formula provides an approximate guideline for the maximum number 
	    of hours you should commit to a job (it may need to be reduced to accommodate
	     job preparation and travel time):<br /><br />
        Maximum Work Hours per Week  =  60 - 3.5 x Credit Hours<br /><br />
      Caution! Don't let your experience in high school and/or a community college 
      tempt you to underestimate the time you will need to be successful in chemical
       engineering at BYU. It is a common (and painful) mistake made by many new students.
        Adhere to the above formula for at least your first year. Then, you will know
         better how to adjust this formula for your own personal circumstances.</p>
