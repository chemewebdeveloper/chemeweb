<h2>Morris D. Argyle</h2>
 <img src="images/directory/argyle.jpg" /><p>Dr. Morris Argyle grew 
 up in Idaho Falls, ID and received his B.S. degree in Chemical Engineering from Brigham 
 Young University in 1990. Following graduation, he worked for over seven years at 
 ExxonMobil's Baytown, Texas refinery, in a number of positions, including process 
 design, operations support, coordination and economics, and as a technical and an 
 operations supervisor.</p>
 <p>Argyle's research interests lie in determining the structure/function relationships of heterogeneous catalysts using 
 spectroscopic techniques, plasma reaction engineering, coal gasification, and new methods for carbon dioxide capture and storage.</p>

<p>He and his wife, Stephanie, who is also a chemical engineer (University of Michigan, 1991), have five children.</p>

