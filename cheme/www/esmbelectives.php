	 <h2>Innovation</h2>
	 	<p>Beginning in Fall 2009,  at least 1 technical elective hour must 
	 	be from a course involving a significant learning experience in creativity 
	 	and/or innovation. Such courses are those that teach concepts and require 
	 	a project in which innovation or creativity is demonstrated. These courses 
	 	include CH EN 199R, 400, 498R, CHEM 497R, and other similar courses from 
	 	EMSB departments that are approved by the student's advisor.  Please note
	 	 that simply taking CH EN 199R, 498R or CHEM 497R does not automatically qualify
	 	  as innovation.  The innovative/creative component must be demonstrated.</p>
