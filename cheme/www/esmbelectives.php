	 <h2>EMSB (Engineering/Math/Science/Business) Elective</h2>
	 	<p>In general (exceptions are noted below), courses that are 300 
	 	level or above, which are "engineering, math, science, or business,"
	 	 may be taken to fulfill EMSB elective.  Business is defined as courses
	 	  taught in the Marriot School of Management.  Math and Science are defined 
	 	  as courses taught in the College of Physical and Mathematical Sciences or 
	 	  the College of Life Sciences (with the exception of the Department of 
	 	  Excercise Science).  Engineering (in the EMSB context) is defined as courses 
	 	  taught in the College of Engineering (including the School of Technology). 
	 	   In addition to these, the following courses also count towards the EMSB
	 	    elective.</p>
