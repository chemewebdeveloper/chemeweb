	<h2>Engineering Elective</h2>
		<p>Courses taken to fulfill the Engineering Elective must meet the ABET 
		definition of "engineering topics" which include "engineering sciences" 
		and "engineering design." The engineering sciences have their roots in 
		mathematics and basic sciences but carry knowledge further toward creative 
		application.  Engineering design is the process of devising a system,
		 component, or process to meet desired needs.</p>
		<p>In general (exceptions are noted below), courses that are 300 level
		or above, which are taught in the departments of Chemical Engineering,
		 Civil and Environmental Engineering, Electrical and Computer Engineering,
